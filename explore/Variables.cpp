#include <iostream>
using namespace std;

int main()
{ // Primitive Datatypes
    int in = 10;
    char ch = 'A';
    float f = 10.99f; // f should be written to tell that it is float.
    double d = 10.99;
    bool b = true; // true is stored as 1 in memory.
    cout << "integer : " << in << endl;
    cout << "characterr : " << ch << endl;
    cout << "float : " << f << endl;
    cout << "double : " << d << endl;
    cout << "bool : " << b << endl;
    return 0;
}